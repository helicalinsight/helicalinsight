@WebAppConfiguration
@RunWith(SpringJUnit4ClassRunner.class)
@ContextConfiguration(locations = { "classpath:application-context.xml", "classpath:dispatcher-servlet.xml",
		"classpath:spring-security.xml" })
@FixMethodOrder(MethodSorters.NAME_ASCENDING)

public class MapPropertiesTest {
	MockMvc efwMock;
	MockMvc mockMvc;
	@Autowired
	FilterChainProxy filterChainProxy;
	
	@Autowired
	IntegrationTestUtility testutility;

	@Autowired
	private WebApplicationContext context;

	@Autowired
	ResourceAuthenticationAndAuthorizationFilter authenticationAndAuthorizationFilter;

	@Bean
	public FileSystemOperationsController fileSystemOperationsController() {
		return new FileSystemOperationsController();
	}

	@Before
	@Transactional
	public void setup() {
		this.mockMvc = MockMvcBuilders.standaloneSetup(this.fileSystemOperationsController).addFilters(filterChainProxy)
				.build();
		ServletContext servletContext = context.getServletContext();
		servletContext.setAttribute("filterStatus", "ok");
		this.efwMock = MockMvcBuilders.webAppContextSetup(this.context)
				.addFilters(filterChainProxy, authenticationAndAuthorizationFilter).build();
	}

	@Autowired
	private FileSystemOperationsController fileSystemOperationsController;

	@Test
	public void getPropertiesMap() throws Exception {
		
		 MockHttpServletRequestBuilder mockHttpServletRequestBuilder = MockMvcRequestBuilders
	                .get("/applicationSettings");
	        List<String> sourceList = new ArrayList<>();
	        sourceList.add("");
	        Map<String, String> map = new HashMap<>();
	        RequestBuilder builder = TestUtility.getMockHttpServletRequestBuilder(mockHttpServletRequestBuilder, map);
	        MvcResult result = this.efwMock.perform(builder).andExpect(MockMvcResultMatchers.status().isOk())
	                .andReturn();
	        JSONObject object = JSONObject.fromObject(result.getResponse().getContentAsString());
	        Assert.assertTrue(object.containsKey("map"));
	        Assert.assertTrue(object.getJSONObject("map").containsKey("mapbox"));
	        Assert.assertTrue(object.getJSONObject("map").getJSONObject("mapbox").containsKey("token"));
	}
}
